  function init() {
    const saved = localStorage.getItem('vw-lang');
    if (saved && TRANSLATIONS[saved]) {
      currentLang = saved;
    }
    updateUI();
  }

  function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('vw-lang', lang);
    updateUI();
    
    // Dispatch event for other components to update
    window.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
  }

  function updateUI() {
    const dict = TRANSLATIONS[currentLang];
    
    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const translation = get(key);
      if (translation !== key) el.innerHTML = translation;
    });

    // Localize standalone numbers
    document.querySelectorAll('[data-i18n-num]').forEach(el => {
      const val = el.getAttribute('data-i18n-num');
      el.textContent = num(val);
    });

    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (dict[key]) {
        const localizedText = num(dict[key]);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = localizedText;
        } else {
          el.textContent = localizedText;
        }
      }
    });

    // Handle special cases (HTML content)
    const title1 = document.querySelector('[data-i18n-html="hero_title_1"]');
    if (title1) title1.textContent = dict.hero_title_1;
    
    const titleSpan = document.querySelector('[data-i18n-html="hero_title_span"]');
    if (titleSpan) titleSpan.textContent = dict.hero_title_span;

    // Update language selector active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('lang-btn--active', btn.dataset.lang === currentLang);
    });

    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
  }

  function get(key) {
    const dict = TRANSLATIONS[currentLang] || TRANSLATIONS['en'];
    const val = dict[key] || TRANSLATIONS['en'][key] || key;
    return num(val);
  }

  function getParty(partyCode) {
    if (!partyCode) return '';
    let key = `party_${partyCode.toLowerCase().replace(/[^a-z]/g, '')}`;
    
    // Map long names to their short keys
    const longToShort = {
      'party_bharatiyajanataparty': 'party_bjp',
      'party_indiannationalcongress': 'party_inc',
      'party_samajwadiparty': 'party_sp',
      'party_allindiatrinamoolcongress': 'party_aitc',
      'party_dravidamunnetrakazhagam': 'party_dmk',
      'party_janatadalunited': 'party_jdu',
      'party_shivasena': 'party_ss',
      'party_nationalistcongressparty': 'party_ncp',
      'party_aamaadmiparty': 'party_aap',
      'party_bahujansamajparty': 'party_bsp',
      'party_telugudesamparty': 'party_tdp',
      'party_yuvajanasramikarythucongressparty': 'party_yvsrcp'
    };
    
    if (longToShort[key]) {
      key = longToShort[key];
    }
    
    const dict = TRANSLATIONS[currentLang] || TRANSLATIONS['en'];
    return dict[key] || partyCode;
  }

  const DIGITS = {
    hi: ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'],
    mr: ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'],
    gu: ['૦', '૧', '૨', '૩', '૪', '૫', '૬', '૭', '૮', '૯']
  };

  function num(n) {
    if (n === null || n === undefined) return '';
    const str = String(n);
    if (currentLang === 'en' || !DIGITS[currentLang]) return str;
    return str.replace(/[0-9]/g, d => DIGITS[currentLang][parseInt(d)]);
  }

  return { init, setLanguage, get, getParty, num, updateUI, currentLang: () => currentLang };
